# Estructura Básica de un Bucle for

# Sintaxis
# for variable in lista
# do
#     # comandos a ejecutar
# done

# variable toma el valor de cada elemento de la lista en cada iteración.
# lista puede ser una serie de valores separados por espacios o generados dinámicamente.

for fruta in manzana pera naranja
do
    echo "Fruta: $fruta"
done

